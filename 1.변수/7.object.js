// 객체 (Object)는 복합 데이터, 여러 가지 데이터들을 함께 묶어놓은것
// {key:value};
// ex. { id:1234, key:'seceret-key'};

let apple = {
  name: "apple",
  color: "red",
  display: "🍎",
};
console.log(apple);
console.log(apple.name);
console.log(apple.color);
console.log(apple.display);

let orange = {
  name: "오렌지",
  color: "orange",
  display: "🍊",
};
console.log(orange);
console.log(orange.name);
console.log(orange.color);
console.log(orange.display);
